import catalogJson from './generated/catalog.json';

export interface DonorInfo {
  name: string;
  brand: string;
  model: string;
  year: string;
  body: string;
  engine: string;
  mileage: string;
  color: string;
  transmission: string;
  drive: string;
  vin: string;
  video: string;
  steeringWheel: string;
  trim: string;
}

export interface CatalogProduct {
  id: number;
  sku: string;
  title: string;
  donorId: string;
  brand: string;
  model: string;
  year: string;
  body: string;
  engine: string;
  imageUrl: string;
  photos: string[];
  color: string;
  conditionRaw: string;
  condition: string;
  isNew: boolean;
  price: number;
  priceFormatted: string;
  outOfStock: boolean;
  categoryId: string;
  oem: string;
  position: string;
  crossNumbers: string[];
  description: string;
  donor: DonorInfo | null;
}

export const allProducts: CatalogProduct[] = (catalogJson as CatalogProduct[]).map(p => ({
  ...p,
  outOfStock: p.price <= 0,
}));

// Home page featured: first 6 items with photo and price
export const products = allProducts
  .filter(p => p.imageUrl && p.price > 0)
  .slice(0, 6)
  .map(p => ({
    id: p.id,
    title: p.title,
    sku: p.oem || p.sku,
    price: p.priceFormatted,
    imageUrl: p.imageUrl,
    isNew: p.isNew,
    isSale: false,
    outOfStock: p.outOfStock,
  }));

